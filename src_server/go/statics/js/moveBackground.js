(function(global){
	"use strict";

	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
	// ----------------------------------------------------------------

	var pos = 0;
	window.setInterval(function(){
		var x = (pos * 2) % 240;
		var y = (pos * 1) % 240;
		document.body.style.backgroundPosition = x + "px " + y + "px";
		pos++;
	}, 50);

	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
})(this);
